          <h2>Pozemek, na kterém hospodaříme</h2>
          <p>
            Pozemek se nachází v katastru Olomouc – Řepčín, uprostřed území tzv. „Hejčínské louky“. Je to příměstská oblast, kde zemědělská činnost prolíná s rekreačním ruchem, a přitom je zde příroda obdivuhodně živá až divoká. V současné době tedy hospodaříme na pozemku o celkové rozloze 1,5 hektaru, z čehož švestkový sad tvoří 0,5 hektaru a jeden hektar je využíván pro pěstování zeleniny.
          </p><p>
            Rádi bychom zde navázali na tradici našich předků, kteří v této krajině hospodařili ještě před nástupem intenzivního zemědělského využívání. Pozemek, na kterém hospodaříme, máme v dlouhodobém pronájmu od paní Milušky Hufové, rodačky z Řepčína. Rodina paní Hufové využívala pozemek pro pěstování zeleniny po desetiletí.
          </p><p>
